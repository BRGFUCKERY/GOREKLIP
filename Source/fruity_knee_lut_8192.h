inline float processSample(float x) noexcept {
    const float sign = (x >= 0.0f ? 1.0f : -1.0f);
    const float ax = std::fabs(x);
    if (ax <= kKneeStart) return x;
    if (ax >= kKneeEnd) return sign * 1.0f;
    const float t = (ax - kKneeStart) / (kKneeEnd - kKneeStart);
    const float pos = t * float(kTableSize - 1);
    const std::size_t idx = static_cast<std::size_t>(pos);
    const std::size_t next = (idx + 1u < kTableSize ? idx + 1u : idx);
    const float frac = pos - float(idx);
    float y = kLut[idx] + (kLut[next] - kLut[idx]) * frac;
    y = (y > 1.0f ? 1.0f : y);
    return sign * y;
}
